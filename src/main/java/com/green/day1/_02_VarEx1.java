package com.green.day1;

public class _02_VarEx1 { //클래스명: varEx1
    public static void main(String[] args) { //메인 메소드명: args(매개변수)
        int year = 0; //변수선언 (변수타입: int, 변수명 year), 0=리터럴,상수
        int age = 14;
        int a = 1, b = 2, c = 3;

        System.out.println(year);

        year = 100;
        System.out.println(year);
        System.out.println(age);
        System.out.println(age + age + age);
        System.out.println(a + b + c);
        System.out.println(c - a);
        }
    }